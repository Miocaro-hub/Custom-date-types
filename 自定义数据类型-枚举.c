#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>

//枚举
//1.枚举类型的定义
//2.枚举的优点
//3.枚举的使用

//枚举:顾名思义就是一一列举,把可能的取值一一列举

//1.枚举类型的定义
enum Sex  //性别
{
	//枚举的可能取值-常量(可以赋初始值)
	MALE,  //0
	FEMALE,  //1
	SECRET  //2
};
int main()
{
	enum Sex s = MALE;  //(赋值时不能赋枚举的可能取值的另外的值)
	printf("%d %d %d\n", MALE, FEMALE, SECRET);  //打印结果为0,1,2(从赋的初始值开始打印,顺次依次加1)
	return 0;
}


//2.枚举的优点

//1.增加代码的可读性和可维护性
//2.和#define定义和标识符比较枚举有类型检查,更加严谨
//3.防止了命名污染(封装)
//4.便于调试
//5.为了方便,一次可以定义多个常量


//3.枚举的使用
//通过枚举类型创建变量,然后进行赋值